void main(){

  //int  = integer
  int a=10; 
  print(a); //output 10
  print(a.runtimeType); //output int

  //double = floating point number
  double b=10.5;
  print(b); //output 10.5
  print(b.runtimeType); //output double
  double b2 =4;
  print(b2); //output 4.0

  //bool
  bool d=true;
  print(d); //output true
  print(d.runtimeType); //output bool

  //String
  String c="Hello";
  print(c); //output Hello
  print(c.runtimeType); //output String
  

}